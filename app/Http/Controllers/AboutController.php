    {
        // Create a new instance of the 'About' model and set its properties
        $about = new About();
        $about->tittle = $request->input('tittle');
        $about->approach = $request->input('approach');
        $about->values = $request->input('values');
        $about->commitment = $request->input('commitment');
        $about->community_engagement = $request->input('community_engagement');
        $about->description = $request->input('description');

        // Save the data to the 'abouts' table
        $saved = $about->save();

        return redirect()->back()->with('success', 'Data Saved Successfully ');

    }

